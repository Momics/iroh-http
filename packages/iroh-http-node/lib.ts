/**
 * iroh-http-node — Node.js entry point.
 *
 * Wires the napi-rs native addon to iroh-http-shared and exports `createNode`.
 *
 * ```ts
 * import { createNode } from 'iroh-http-node';
 *
 * const node = await createNode({ key: savedKey });
 * node.serve({}, req => new Response('hello'));
 * const res = await node.fetch(peerId, '/api');
 * ```
 */

// The napi-rs build tool generates this file at build time.
// It loads the correct platform-specific .node binary.
import {
  createEndpoint,
  closeEndpoint,
  jsNextChunk,
  jsSendChunk,
  jsFinishBody,
  jsAllocBodyWriter,
  jsCancelRequest,
  jsNextTrailer,
  jsSendTrailers,
  jsAllocFetchToken,
  jsCancelInFlight,
  rawFetch as napiRawFetch,
  rawServe as napiRawServe,
  rawRespond as napiRawRespond,
  rawConnect as napiRawConnect,
} from "./index.js";  // generated by napi build

import {
  buildNode,
  type Bridge,
  type RawFetchFn,
  type RawServeFn,
  type RawConnectFn,
  type AllocBodyWriterFn,
  type NodeOptions,
  type IrohNode,
  type FfiResponse,
  type FfiDuplexStream,
  type RequestPayload,
  type FfiResponseHead,
  classifyBindError,
  type SecretKey,
} from "@momics/iroh-http-shared";

// ── Bridge implementation ─────────────────────────────────────────────────────

const bridge: Bridge = {
  nextChunk: (handle: number) => jsNextChunk(handle),
  sendChunk: (handle: number, chunk: Uint8Array) => jsSendChunk(handle, chunk),
  finishBody: (handle: number) => {
    jsFinishBody(handle);
    return Promise.resolve();
  },
  cancelRequest: (handle: number) => {
    jsCancelRequest(handle);
    return Promise.resolve();
  },
  allocFetchToken: () => Promise.resolve(jsAllocFetchToken()),
  cancelFetch: (token: number) => { jsCancelInFlight(token); },
  nextTrailer: async (handle: number) => {
    const rows = await jsNextTrailer(handle);
    return rows ? (rows as string[][]).map((p) => [p[0], p[1]] as [string, string]) : null;
  },
  sendTrailers: (handle: number, trailers: [string, string][]) => {
    jsSendTrailers(handle, trailers as string[][]);
    return Promise.resolve();
  },
};

// ── Platform function adapters ────────────────────────────────────────────────

const rawFetch: RawFetchFn = async (
  endpointHandle,
  nodeId,
  url,
  method,
  headers,
  reqBodyHandle,
  fetchToken,
  directAddrs
) => {
  const res = await napiRawFetch(
    endpointHandle,
    nodeId,
    url,
    method,
    headers as string[][],
    reqBodyHandle ?? null,
    fetchToken,
    directAddrs ?? null
  );
  return {
    status: res.status,
    headers: res.headers as [string, string][],
    bodyHandle: res.bodyHandle,
    url: res.url,
    trailersHandle: res.trailersHandle,
  } satisfies FfiResponse;
};

const rawServe: RawServeFn = (
  endpointHandle,
  _options,
  callback: (payload: RequestPayload) => Promise<FfiResponseHead>
) => {
  napiRawServe(
    endpointHandle,
    (payload: Record<string, unknown>) => {
      const typed = payload as unknown as RequestPayload;
      // napi-rs's ThreadsafeFunction does not await Promise return values,
      // so we handle the async work here and call rawRespond explicitly.
      callback(typed)
        .then((head) => {
          napiRawRespond(typed.reqHandle, head.status, head.headers as string[][]);
        })
        .catch((err: unknown) => {
          console.error("[iroh-http-node] serve handler error:", err);
          napiRawRespond(typed.reqHandle, 500, []);
        });
    }
  );
};

const allocBodyWriter: AllocBodyWriterFn = () => jsAllocBodyWriter();

const rawConnect: RawConnectFn = async (
  endpointHandle,
  nodeId,
  path,
  headers
) => {
  const ffi = await napiRawConnect(
    endpointHandle,
    nodeId,
    path,
    headers as string[][]
  );
  return {
    readHandle: ffi.readHandle,
    writeHandle: ffi.writeHandle,
  } satisfies FfiDuplexStream;
};

// ── Public API ────────────────────────────────────────────────────────────────

/** Normalise `relayMode` into flat fields for the Rust adapter. */
function normaliseRelayMode(mode?: import("@momics/iroh-http-shared").RelayMode): {
  relays: string[] | undefined;
  disableNetworking: boolean;
} {
  if (mode === "disabled") return { relays: [], disableNetworking: true };
  if (mode === "default" || mode === undefined) return { relays: undefined, disableNetworking: false };
  if (Array.isArray(mode)) return { relays: mode, disableNetworking: false };
  return { relays: [mode], disableNetworking: false };
}

/**
 * Create an Iroh node for peer-to-peer HTTP.
 *
 * @param options Optional configuration.  Omit `key` to generate a fresh identity.
 */
export async function createNode(options?: NodeOptions): Promise<IrohNode> {
  const keyBytes = options?.key
    ? (options.key instanceof Uint8Array ? options.key : (options.key as SecretKey).toBytes())
    : undefined;

  const { relays, disableNetworking } = normaliseRelayMode(options?.relayMode);

  const info = await createEndpoint(
    options
      ? {
          key: keyBytes,
          idleTimeout: options.idleTimeout,
          relays,
          dnsDiscovery: options.dnsDiscovery,
          channelCapacity: options.channelCapacity,
          maxChunkSizeBytes: options.maxChunkSizeBytes,
          maxConsecutiveErrors: options.maxConsecutiveErrors,
          discovery: options.discovery
            ? {
                mdns: options.discovery.mdns ?? false,
                serviceName: options.discovery.serviceName,
                advertise: options.discovery.advertise ?? true,
              }
            : undefined,
          drainTimeout: options.drainTimeout,
          handleTtl: options.handleTtl,
          disableNetworking,
        }
      : undefined
  ).catch((e: unknown) => { throw classifyBindError(e); });

  return buildNode(
    bridge,
    {
      endpointHandle: info.endpointHandle,
      nodeId: info.nodeId,
      keypair: info.keypair as Uint8Array,
    },
    rawFetch,
    rawServe,
    rawConnect,
    allocBodyWriter,
    (handle) => closeEndpoint(handle)
  );
}
